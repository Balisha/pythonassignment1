# initial string
s = "Python"
# calculate length of the list
stringlength=len(s)
# slicing
slicedString = s[stringlength::-1]
# print the reversed string
print (slicedString)

#get all the items
my_list = [1, 2, 3, 4, 5]
print(my_list[:])

#get all the items after specific position

print(my_list[2:])

#get all the items before specific position

print(my_list[:2])

#get all the items from one position to another position

print(my_list[2:4])

#get items at specified interval using steps

print(my_list[1:4:2])